-- Users_History

CREATE OR REPLACE TRIGGER Users_History_trig
AFTER INSERT OR UPDATE OR DELETE ON USER_T
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO USER_HISTORY (USER_HISTORY_ID, TIME_STAMP, USER_ID, USER_ID_Hist, USERNAME, EXPIRATION_DATE, STATUS, CREATED_DATE)
        VALUES (USERS_HISTORY_INC.NEXTVAL,CURRENT_TIMESTAMP,:new.USER_ID, :new.USER_ID, :new.USERNAME , :new.EXPIRATION_DATE ,:new.STATUS ,:new.CREATED_DATE);
    ELSIF UPDATING THEN
        UPDATE USER_HISTORY
		SET TIME_STAMP = CURRENT_TIMESTAMP, USER_ID = :new.USER_ID, USERNAME = :new.USERNAME , EXPIRATION_DATE = :new.EXPIRATION_DATE, STATUS = :new.STATUS , CREATED_DATE = :new.CREATED_DATE
		WHERE USER_ID = :new.USER_ID;
    ELSIF DELETING THEN
		UPDATE USER_HISTORY
		SET USER_ID = NULL 
		WHERE USER_ID = :new.USER_ID;
	END IF;
END;


